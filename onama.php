Woodstock nije bio puki rock festival kakvih je tog ljeta bilo diljem Amerike: tamo se odlazilo iz uvjerenja i iz protesta. I to u velikom broju. Morala je reagirati i vojska osiguravajući prolaz znatiželjnima, ali i prijevoz sudionika helikopterima do bine, nužnu medicinsku pomoć te hranu i vodu. No, ukupno gledajući, strahu nije bilo mjesta, Woodstock naciji je jedino "prijetio" oblak dima marihuane brojnih sudionika te kiša. U tolikoj gomili bilo je sasvim jasno kako je sigurnost ugrožena: ondje je bilo nekoliko stotina tisuća ljudi, vjerojatno i cijeli milijun, tri dana glazbe, ljubavi, mira, a sve bez ijednog ozbiljnijeg incidenta. Nikad se ništa sličnog nije dogodilo u povijesti čovječanstva. Samo pet osoba je umrlo (troje od predoziranja, jedna od puknuća slijepog crijeva dok je jednog usnulog tinejdžera pregazio traktor), ali su tijekom tri dana rođene tri bebe. Tu se dogodio vrhunac zanosa i prosvjeda cijele jedne generacije...

